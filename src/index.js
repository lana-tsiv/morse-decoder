const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  return expr
    .replace(/\*/g, " ")
    .split(/(.{10})/)
    .filter((e) => e)
    .map(
      (e) =>
        MORSE_TABLE[
          e
            .slice(e.indexOf("1"), e.length)
            .replace(/10/g, ".")
            .replace(/11/g, "-")
        ]
    )
    .map((c) => (c ? c : " "))
    .join("");
}

module.exports = {
  decode,
};
